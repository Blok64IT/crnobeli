                        <h2>FK Partizan Beograd</h2>
                        <p class="bold">Zemlja: Srbija Grad: Beograd Osnovan: 4. oktobar 1945. (69)
                            Trofeji: 25 titula prvaka države, 12 Kupova, 1 Superkup Jugoslavije, 1 Mitropa kup, finalista Kupa šampiona 1966.</p>
                        <p>"Parni valjak" pošao je putem moskovskog CSKA, a na kraju se po bojama ugledao na italijanski Juventus. Sve se dogodilo slučajno i spontano, da bi danas srca milionske armije navijača Partizana kucala za crno-bele boje.Beogradski sastav početkom pedesetih godina prošlog veka bio je pobednik međunarodnog turnira u Južnoj Americi. S obzirom da je igrom oduševio, posebno ljude iz Juventusa, koji je takođe bio na turneji, torinski klub je igračima Partizana poklonio dve garniture crno-belih dresova.Tačna godina kada je crno-bela usvojena kao zvanična kombinacija boja Partizana nije poznata. Pretpostavlja se da se to dogodilo početkom ili sredinom pedesetih. Od 1958. godine grb Partizana postaje crno-beli.</p>
