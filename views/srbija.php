                        <h2>FK ČSK Pivara Čelarevo</h2>
                        <p class="bold">FK ČSK Pivara je srpski fudbalski klub iz Čelareva, Opština Bačka Palanka. Klub je osnovan 1925. godine i trenutno se takmiči u Prvoj ligi Srbije, drugom takmičarskom nivou srpskog fudbala.</p>
                        <p>Po osnivanju 1925. dobija ime ČOSK (Čebski omladinski sportski klub). Čelarevo današnje ime dobija posle Drugog svetskog rata, do tada se zvalo Čeb. 1946. godine klub ponovo oživljava u ovom mestu pod imenom FK Radnički, da bi 1950. godine promenio ime u SK Podunavlje, kada i beleži zapaženije rezultate, plasmanom u Grupno prvenstvo Novog Sada. Taj uspon traje dve godine, posle čega se takmiči u područnoj ligi Novog Sada. 1953. godine fuzijom „Podunavlja“ i „Krajišnika“ formira se danasnji ČSK. Novi klub je 1963. godine ponovo u višem rangu regionalnoj Podunavskoj ligi. Naredne tri sezone je član Vojvođanske lige, u koju ponovo ulazi 1969. i opet se u njoj zadržava tri sezone.
                        Narednih 17 godina provodi u područnoj i regionalnoj ligi, da bi 1989. po treći put stigao do Vojvođanske lige. Ovoga puta je u njoj samo jednu sezonu, ali se odmah sledeće vraća. 1992/93. ostvaruje svoj najveći uspeh do tada takmičeći se po prvi put u republičkom rangu, Srpskoj ligi- Sever. 1998. je u višem rangu. U Drugoj saveznoj ligi ČSK se takmičio pune četiri sezone, da bi se ponovo našao u Srpskoj ligi - Vojvodina. U sezoni 2004/05. osvaja drugo mesto u Srpskoj ligi Vojvodina i nakon 2 kruga baraža, gde je bio uspešniji od „Posavac Kneževca“ 1:1, 5:1 i Proletera iz Zrenjanina 2:1, 0:0 ulazi u Drugu saveznu ligu. U Prvoj ligi (2. rang) ostaje do sezone 2009/10. kada zauzima pretposlednje 17. mesto i ispada u niži rang. Od tada se takmiči u Srpskoj ligi Vojvodina. U Srpskoj ligi je proveo pet sezona, ali je u sezoni 2014/15. zauzeo prvo mesto i plasirao se u Prvu ligu Srbije.</p>
